#!/usr/bin/env python3
"""Generate simple figures from a dataset label-summary CSV."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Sequence

import matplotlib

matplotlib.use("Agg")

import matplotlib.pyplot as plt
import pandas as pd


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Plot simple figures from a dataset label-summary CSV."
    )
    parser.add_argument(
        "--label-summary-csv",
        required=True,
        help="CSV generated by summarize_chexpert_metadata.py.",
    )
    parser.add_argument(
        "--output-dir",
        required=True,
        help="Directory where PNG figures will be written.",
    )
    return parser


def _plot_metric(dataframe: pd.DataFrame, column: str, title: str, output_path: Path) -> bool:
    metric_series = pd.to_numeric(dataframe[column], errors="coerce")

    if metric_series.isna().all():
        print(f"Skipping {output_path.name}: column {column} has only missing values.")
        return False

    plot_frame = dataframe.loc[metric_series.notna(), ["label_name"]].copy()
    plot_frame[column] = metric_series.loc[metric_series.notna()]

    figure, axis = plt.subplots(figsize=(10, 4.5))
    axis.bar(plot_frame["label_name"], plot_frame[column])
    axis.set_title(title)
    axis.set_xlabel("Label")
    axis.set_ylabel(column)
    axis.tick_params(axis="x", rotation=45)
    figure.tight_layout()
    figure.savefig(output_path, dpi=150)
    plt.close(figure)
    print(output_path)
    return True


def main(argv: Sequence[str] | None = None) -> int:
    args = build_parser().parse_args(argv)

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    dataframe = pd.read_csv(args.label_summary_csv)

    _plot_metric(
        dataframe,
        column="available_count",
        title="Label available counts",
        output_path=output_dir / "label_available_counts.png",
    )
    _plot_metric(
        dataframe,
        column="positive_prevalence",
        title="Label positive prevalence",
        output_path=output_dir / "label_positive_prevalence.png",
    )
    _plot_metric(
        dataframe,
        column="mean_target",
        title="Label mean target",
        output_path=output_dir / "label_mean_target.png",
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
